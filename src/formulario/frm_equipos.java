                txt_estado.setText(Tabla.getValueAt(Tabla.getSelectedRow(), 7).toString());

                tab_equipos.setEnabledAt(1, true);
                tab_equipos.setEnabledAt(0, false);
                tab_equipos.setSelectedIndex(1);

                txt_codigo.setEnabled(false);
