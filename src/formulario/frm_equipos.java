    private boolean ga = false;
    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

        try {
            if (txt_codigo.getText().length() > 0) {
                if (txt_cantidad.getText().length() > 0) {
                    if (txt_color.getText().length() > 0) {
                        if (txt_unitario.getText().length() > 0) {
                            if (txt_estado.getText().length() > 0) {
                                if (txt_marca.getText().length() > 0) {
                                    if (txt_modelo.getText().length() > 0) {
                                        if (txt_nombre.getText().length() > 0) {

                                            clases.cls_equipos obj = new clases.cls_equipos(Integer.parseInt(txt_codigo.getText()), txt_nombre.getText(), txt_marca.getText(), txt_modelo.getText(), Integer.parseInt(txt_cantidad.getText()), txt_color.getText(), Double.parseDouble(txt_unitario.getText()), txt_estado.getText());
                                            if (ga == false) {
                                                String error = obj.Guardar();
                                                if (!"".equals(error)) {
                                                    JOptionPane.showMessageDialog(null, error, "Sistema inventario", JOptionPane.ERROR_MESSAGE);
                                                }
                                            } else {
                                                String error = obj.Actualizar();
                                                if (!"".equals(error)) {
                                                    JOptionPane.showMessageDialog(null, error, "Sistema inventario", JOptionPane.ERROR_MESSAGE);
                                                }
                                                ga = false;
                                            }
                                            limpiar();
                                            LlenarTabla();
                                            tab_equipos.setEnabledAt(0, true);
                                            tab_equipos.setEnabledAt(1, false);
                                            tab_equipos.setSelectedIndex(0);
                                        } else {
                                            JOptionPane.showMessageDialog(null, "Ingrese un codigo", "Sistema inventario", JOptionPane.INFORMATION_MESSAGE);
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(null, "ingrese una cantidad");
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "ingrese un color");
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "ingrese el costo");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "ingrese el estado");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "ingrese la marca");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "ingrese el modelo");
                }
            } else {
                JOptionPane.showMessageDialog(null, "ingrese un nombre");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un codigo", e.getMessage(), JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        int filsel = Tabla.getSelectedRow();
        if (filsel == -1) {

            JOptionPane.showMessageDialog(null, "Por favor selecione una fila");

        }
        clases.cls_equipos obj = new clases.cls_equipos();
        String error = obj.Eliminar(Integer.parseInt(Tabla.getValueAt(Tabla.getSelectedRow(), 0).toString()));
        if (!"".equals(error)) {
            JOptionPane.showMessageDialog(null, error, "Sistema inventario", JOptionPane.ERROR_MESSAGE);

            LlenarTabla();
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed


