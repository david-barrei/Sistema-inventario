 private static boolean ga = false;
    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        try {

            clases.cls_clientes obj = new clases.cls_clientes(Integer.parseInt(txt_cedula.getText()), txt_nombre.getText(), Integer.parseInt(txt_direccion.getText()), txt_telefono.getText(), CortarCodigo(cb_tipoclientes.getSelectedItem().toString()));
            if (ga == false) {
                String error = obj.guardar();
                if (!"".equals(error)) {
                    JOptionPane.showMessageDialog(null, error, "Sistema Inventario", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                String error = obj.actualizar();
                if (!"".equals(error)) {
                    JOptionPane.showMessageDialog(null, error, "Sistema Inventario", JOptionPane.ERROR_MESSAGE);
                }
                ga = false;
            }
            obj.CargarTabla(tabla_clientes);
            mtd_limpiar();
            tab_clientes.setEnabledAt(0, true);
            tab_clientes.setEnabledAt(1, false);
            tab_clientes.setSelectedIndex(0);
            panel_buscar.setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Sistema Inventario", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        cls_conexion cn = new cls_conexion();
        if (cb_combo.getSelectedItem().toString().equals("*")) {
            cn.CargarTabla("Select cod_cli as CODIGO,nom_cli as NOMBRE,dir_cli as DIRECCION, tel_cli as TELEFONO, id_tip_cli as TIPO_CLIENTE From clientes", tabla_clientes);

        }
        if (cb_combo.getSelectedItem().toString().equals("Codigo")) {
            cn.CargarTabla("Select cod_cli as CODIGO,nom_cli as NOMBRE,dir_cli as DIRECCION ,tel_cli as TELEFONO ,id_tip_cli as TIPO_CLIENTE From clientes Where (cod_cli=" + txt_buscar.getText() + ")", tabla_clientes);

        }
        if (cb_combo.getSelectedItem().toString().equals("Nombre")) {
            cn.CargarTabla("Select cod_cli as CODIGO,nom_cli as NOMBRE, dir_cli as DIRECCION ,tel_cli as TELEFONO ,id_tip_cli as TIPO_CLIENTE From clientes Where (nom_equ='" + txt_buscar.getText() + "')", tabla_clientes);

        }
    }//GEN-LAST:event_btn_buscarActionPerformed

