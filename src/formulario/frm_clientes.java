    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed

        if (tabla_clientes.getSelectedRow() >= 0) {
            clases.cls_clientes obj = new clases.cls_clientes();
            String error = obj.eliminar(Integer.parseInt(tabla_clientes.getValueAt(tabla_clientes.getSelectedRow(), 0).toString()));
            if (!"".equals(error)) {
                JOptionPane.showMessageDialog(null, error, "Sistema Inventario", JOptionPane.ERROR_MESSAGE);
            }
            obj.CargarTabla(tabla_clientes);
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila!!", "Sistema Inventario", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

